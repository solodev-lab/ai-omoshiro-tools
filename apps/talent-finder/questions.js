// 才能タイプ判定関数
function determineTalent(answers) {
    var scores = {};
    var keys = Object.keys(TALENT_TYPES);
    var i;
    for (i = 0; i < keys.length; i++) {
        scores[keys[i]] = 0;
    }

    for (i = 0; i < answers.length; i++) {
        var scoringIndex = i * 4 + answers[i];
        if (SCORING[scoringIndex]) {
            var entries = SCORING[scoringIndex];
            for (var j = 0; j < entries.length; j++) {
                scores[entries[j][0]] = (scores[entries[j][0]] || 0) + entries[j][1];
            }
        }
    }

    var maxScore = 0;
    var resultType = keys[0];
    for (i = 0; i < keys.length; i++) {
        if (scores[keys[i]] > maxScore) {
            maxScore = scores[keys[i]];
            resultType = keys[i];
        }
    }

    return resultType;
}
