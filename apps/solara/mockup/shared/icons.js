  function getZodiacFromBirthday(dateStr) {
    if (!dateStr) return 'scorpio';
    var parts = dateStr.split(/[-\/]/);
    var m = parseInt(parts[1], 10);
    var d = parseInt(parts[2], 10);
    if (!m || !d) return 'scorpio';
    if ((m===3 && d>=21) || (m===4 && d<=19)) return 'aries';
    if ((m===4 && d>=20) || (m===5 && d<=20)) return 'taurus';
    if ((m===5 && d>=21) || (m===6 && d<=20)) return 'gemini';
    if ((m===6 && d>=21) || (m===7 && d<=22)) return 'cancer';
    if ((m===7 && d>=23) || (m===8 && d<=22)) return 'leo';
    if ((m===8 && d>=23) || (m===9 && d<=22)) return 'virgo';
    if ((m===9 && d>=23) || (m===10 && d<=22)) return 'libra';
    if ((m===10 && d>=23) || (m===11 && d<=21)) return 'scorpio';
    if ((m===11 && d>=22) || (m===12 && d<=21)) return 'sagittarius';
    if ((m===12 && d>=22) || (m===1 && d<=19)) return 'capricorn';
    if ((m===1 && d>=20) || (m===2 && d<=18)) return 'aquarius';
    return 'pisces';
  }

  /* Animated canvas nebula background */
  function initCosmicCanvas(canvas, sign) {
    var cols = ZODIAC_COLORS[sign] || ZODIAC_COLORS.scorpio;
    var ctx = canvas.getContext('2d');
    var w = canvas.width = canvas.offsetWidth || window.innerWidth;
    var h = canvas.height = canvas.offsetHeight || window.innerHeight;
    var t = Math.random() * 100;

    function anim() {
      ctx.clearRect(0, 0, w, h);
      /* Base gradient */
      var base = ctx.createRadialGradient(w*0.5, h*0.5, 0, w*0.5, h*0.5, w*0.7);
      base.addColorStop(0, '#0e1e38');
      base.addColorStop(1, '#080C14');
      ctx.fillStyle = base;
      ctx.fillRect(0, 0, w, h);

      /* Nebula glow 1 — primary color, moves */
      var x1 = w*0.5 + Math.sin(t*0.5) * w*0.25;
      var y1 = h*0.4 + Math.cos(t*0.35) * h*0.2;
      var g1 = ctx.createRadialGradient(x1, y1, 0, x1, y1, w*0.5);
      g1.addColorStop(0, 'rgba('+cols.c1[0]+','+cols.c1[1]+','+cols.c1[2]+',0.12)');
      g1.addColorStop(0.4, 'rgba('+cols.c1[0]+','+cols.c1[1]+','+cols.c1[2]+',0.04)');
      g1.addColorStop(1, 'transparent');
      ctx.fillStyle = g1;
      ctx.fillRect(0, 0, w, h);

      /* Nebula glow 2 — secondary color, moves opposite */
      var x2 = w*0.35 + Math.cos(t*0.3) * w*0.2;
      var y2 = h*0.65 + Math.sin(t*0.45) * h*0.15;
      var g2 = ctx.createRadialGradient(x2, y2, 0, x2, y2, w*0.4);
      g2.addColorStop(0, 'rgba('+cols.c2[0]+','+cols.c2[1]+','+cols.c2[2]+',0.09)');
      g2.addColorStop(0.5, 'rgba('+cols.c2[0]+','+cols.c2[1]+','+cols.c2[2]+',0.03)');
      g2.addColorStop(1, 'transparent');
      ctx.fillStyle = g2;
      ctx.fillRect(0, 0, w, h);

      /* Nebula glow 3 — white/subtle highlight at top */
      var x3 = w*0.6 + Math.sin(t*0.25) * w*0.15;
      var y3 = h*0.15 + Math.cos(t*0.2) * h*0.08;
      var g3 = ctx.createRadialGradient(x3, y3, 0, x3, y3, w*0.3);
      g3.addColorStop(0, 'rgba(255,255,255,0.03)');
      g3.addColorStop(1, 'transparent');
      ctx.fillStyle = g3;
      ctx.fillRect(0, 0, w, h);

      t += 0.004;
      requestAnimationFrame(anim);
    }
    anim();
  }

