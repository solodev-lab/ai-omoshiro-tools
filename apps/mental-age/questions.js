// メンタル年齢を計算する関数
function calculateMentalAge(answers) {
    var total = 0;
    for (var i = 0; i < answers.length; i++) {
        total += QUESTIONS[i].ageWeights[answers[i]];
    }
    var avg = total / QUESTIONS.length;
    // ランダム変動 ±3
    var variation = Math.floor(Math.random() * 7) - 3;
    var age = Math.round(avg) + variation;
    // 3〜80にクランプ
    if (age < 3) age = 3;
    if (age > 80) age = 80;
    return age;
}

